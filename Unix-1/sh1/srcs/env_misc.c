#include "ftminishell1.h"

void		ft_print_tmp_env(char **ev)
{
	int			i;

	i = 0;
	while (ev[i] != NULL)
		i++;
	i--;
	while (i >= 0)
	{
		ft_putendl(ev[i]);
		i--;
	}
}

